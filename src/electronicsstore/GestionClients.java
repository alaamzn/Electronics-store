    public void loadDataFromDataBase(){
        try {
	  int a;
	  
	  PreparedStatement pst = con.prepareStatement("select * from client");
	  ResultSet rs = pst.executeQuery();
	  
	  ResultSetMetaData rd = rs.getMetaData();
	  
	  a = rd.getColumnCount();
	  
	  d = (DefaultTableModel)jTable1.getModel();
	  
	  d.setRowCount(0);
	  
	  while(rs.next()){
	      Vector v = new Vector();
	      for(int i = 0; i < a; i++){
		v.add(rs.getString("id"));
		v.add(rs.getString("nom"));
		v.add(rs.getString("telephone"));
		v.add(rs.getString("addresse"));
		v.add(rs.getString("email"));
	      }
	      d.addRow(v);
	  }
	  
        } catch (SQLException ex) {
	  Logger.getLogger(GestionClients.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
